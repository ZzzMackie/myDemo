function dealSearchData(data) {
    $('#searchContent').html('');
    var listData = data.result;
    
    for (var i = 0; i < listData.length; i++) {
        console.log(listData[i]);
        $('<li><div class = "text_item">'+ listData[i][0] +'</div><div class="text_num">'+ listData[i][1] +'</div></li>').appendTo($('#searchContent'))
        $('#searchContent li .text_item').css({
                        // float:'left',
                         cursor:'pointer',
                         padding: '0 10px',
                         fontSize: 12,
                     });
        $('#searchContent li .text_num').css({
                        float:'right',
                        cursor:'pointer',
                        // padding: '0 0 0 20px',
                        fontSize: 12,
                    });
                    //  console.log($('#searchContent'))
        $('#searchContent').slideDown()
                            .css({
                                maxHeight: 300,
                                // overflowY: ''
                                overflowY: 'scroll'
                            }).on('click', 'li', function (e) {
                                // console.log(e.preventDefault())
                                // console.log(e.stopPropagation())
                                e.preventDefault();
                                e.stopPropagation();
                                // console.log($(this).text())
                                $('.cj-input-search').val($(this).text());
                                $(this).parent().slideUp();
                            });
                            
        $(document).click(function (e) {
            $('#searchContent').slideUp();
        })
    }
    }
    $('#search').inputSearch({
        type: 'GET',
        url: 'https://suggest.taobao.com/sug',
        jsonp: 'callback',
        jsonpCallback: 'dealSearchData',
        dataName: 'q',
        success: dealSearchData,
        inputWidth: 520,
        dataType: 'jsonp',
        btnColor: 'red',
        height: 35,
        fontSize: 12,
    });